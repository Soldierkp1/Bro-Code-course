# Madlibs game
# Word game where you create a story by filling in blanks with random words.

adjective1 = input("Enter an adjective (description): ")
noun1 = input("Enter a noun (place, person, thing): ")
adjective2 = input("Enter a second adjective: ")
verb1 = input("Enter a verb (action, state) in present tense (-ing): ")
adjective3 = input("Enter a third adjective: ")

print(f"Today I went to a {adjective1} zoo.")
print(f"In an exhibit I saw a {noun1}")
print(f"{noun1} was a {adjective2} and {verb1}")
print(f"I was {adjective3}!")